// 01_Introduction/hello_world.cpp
#include <iostream>

// The main function is the entry point of every C++ program
int main() {
    // std::cout is used to print output to the console
    // << is the insertion operator
    // std::endl represents a newline character
    std::cout << "Hello, World!" << std::endl;

    // returning 0 indicates that the program finished successfully
    return 0;
}
